public class Converter {
    public double setValuetoM(double value) {
        return value * 1.0936;
    }

    public double setValuetoCm(double value) {
        return value /0.10936;
    }

    public double setValuetoFeet(double value) {
        return value*3;
    }

    public double setValuetoMiles(double value) {
        return value*0.0085227273;
    }
}